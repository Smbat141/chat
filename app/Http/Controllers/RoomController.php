     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user_id = Auth::user()->id;
        $comments = $this->getComment($user_id);
        $data = [
         'id' => $id,
         'user_id' => $user_id,
         'comments' => $comments,
        ];
        return view('room',$data);

    }

